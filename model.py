class ClassBlock_LinearBN(nn.Module):
    def __init__(self, input_dim, class_num, bias=False):
        super(ClassBlock_LinearBN, self).__init__()
        add_block = []
        add_block += [nn.Linear(input_dim, class_num, bias=bias)]
        add_block += [nn.BatchNorm1d(class_num)]
        add_block = nn.Sequential(*add_block)
        # add_block.apply(weights_init_classifier_no_bias)
        if bias:
            add_block.apply(weights_init_kaiming)
        else:
            add_block.apply(weights_init_kaiming_no_bias)
        self.add_block = add_block
        self.class_num = class_num

    def forward(self, x):
        x = normalize(x)
        self.normalizing_weight()
        # self.centering_weight()
        x = self.add_block(x)
        return x

    def normalizing_weight(self):
        self.add_block[0].weight.data = normalize(self.add_block[0].weight.data)

    def centering_weight(self):
        n = self.class_num
        centering = torch.eye(n) - 1.0 / n * torch.ones((n, n))
        centering = Variable(centering.cuda())
        self.add_block[0].weight.data = torch.mm(centering, self.add_block[0].weight.data)


